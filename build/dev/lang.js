window.addEventListener("DOMContentLoaded", function addLang() {
  var lang = document.getElementById("lang");
  var target = document.querySelectorAll("[data-key]");
  var langue;

  if (localStorage.getItem("langue")) {
    switchLang();
  }

  lang.addEventListener("click", changeCont);
  lang.addEventListener("click", switchLang);

  function changeCont() {
    if (lang.innerText == "عربي") {
      langue = "arabe";
    } else {
      langue = "frensh";
    }
    localStorage.setItem("langue", langue);
  }

  function switchLang() {
    for (let i = 0, all = target.length; i < all; i++) {
      current = target[i].getAttribute("data-key");
      if (AR_LANG[localStorage.getItem("langue")].hasOwnProperty(current)) {
        target[i].innerHTML = AR_LANG[localStorage.getItem("langue")][current];
      }
    }
  }
});
